print("Programa para analizar una palabra")
palabra = input("Escribe una palabra: ")
print(f"La palabra '{palabra}' tiene {len(palabra)} caracteres.")
print(f"La primera letra de la palabra es: '{palabra[0]}'")
print(f"La última letra de la palabra es: '{palabra[-1]}'")
print(f"La 2 primeras letras son: '{palabra[0:2]}'")
print(f"La 2 últimas letras son: '{palabra[-2:]}'")